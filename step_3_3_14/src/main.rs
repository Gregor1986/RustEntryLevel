// В редакторе кода представлен фрагмент кода. Дополните форматную строку в println!() 
// позиционными индексными аргументами в правильном порядке, чтобы получить корректный вывод.
fn main() {
    let (z, y, x) = (100, 50, 30);
    println!("z = {2}, y = {1}, x = {0}", x, y, z); // "z = 100, y = 50, x = 30"
}